from web_scraping.parcers import Parser, search_products_wb


def scrape_wb(search_query, headless=False):
    """
    Выполняет полный цикл парсинга товаров с Wildberries по заданному поисковому запросу.
    Создаёт экземпляр парсера для Wildberries, настраивает драйвер браузера, выполняет
    поиск товаров, сохраняет результаты в базу данных и возвращает сводную статистику.

    Args:
        search_query (str): Поисковый запрос для Wildberries (например, «кроссовки»,
            «платье», «наушники»). Используется для формирования URL поиска на сайте.
        headless (bool, optional): Флаг запуска браузера в headless‑режиме
            (без графического интерфейса). По умолчанию False (режим с отображением окна).
            Рекомендуется использовать True для фоновых задач и серверных запусков.

    Returns:
        dict: Словарь с результатами операции, содержащий следующие ключи:
            - 'saved_count' (int): количество товаров, успешно сохранённых или
              обновлённых в базе данных (с учётом дубликатов и изменений цены);
            - 'total_found' (int): общее количество товаров, найденных на странице
              результатов поиска (до обработки и сохранения);
            - 'search_query' (str): исходный поисковый запрос — для отчётности,
              анализа и логирования;
            - 'product_ids' (list): список ID всех обработанных товаров — может
              использоваться для последующих операций (например, отслеживания цен).
    """
    # Создаём экземпляр парсера для Wildberries с указанием имени маркетплейса
    # и режима запуска (headless или с отображением браузера)
    parser = Parser(name_mp="wildberries.ru", headless=headless)

    # Инициализируем WebDriver браузера: настраиваем опции (включая headless),
    # запускаем браузер и выполняем базовые настройки для парсинга
    parser.setup_driver()

    # Выполняем поиск товаров на Wildberries по переданному поисковому запросу
    # Функция search_products_wb открывает страницу поиска, ждёт загрузки элементов
    # и извлекает данные о товарах с первой страницы результатов
    products_data = search_products_wb(parser, search_query)

    # Сохраняем полученные данные о товарах в базу данных
    # Метод save_products_to_db обрабатывает каждый товар:
    # - проверяет наличие в БД;
    # - создаёт новую запись, если товара нет;
    # - обновляет цену, если она изменилась.
    # Возвращает словарь с результатами сохранения (количество сохранённых, ID и т.д.)
    save_result = parser.save_products_to_db(products_data)

    # Формируем итоговый словарь с результатами для возврата
    return {
        "saved_count": save_result['saved_count'],  # Количество товаров, сохранённых/обновлённых в БД
        "total_found": len(products_data),  # Общее количество найденных товаров
        "search_query": search_query,  # Исходный поисковый запрос — для отчётности и анализа
        "product_ids": save_result['product_ids']  # Список ID всех обработанных товаров
    }
