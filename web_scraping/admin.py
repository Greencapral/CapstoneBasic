@admin.register(Searchers)
class SearchersAdmin(admin.ModelAdmin):
    """
    Админ‑панель для модели Searchers (поисковые запросы пользователей).
    Позволяет управлять записями поисковых запросов: просматривать, искать, фильтровать.
    Отображает связанные продукты и маркетплейсы в виде текстовых списков.
    """

    """Поля и методы, отображаемые в списке поисковых запросов."""
    list_display = (
        "user",  # Отображение пользователя, сделавшего запрос
        "query",  # Отображение текста поискового запроса
        "get_products",  # Отображение списка связанных продуктов (через метод)
        "get_marketplaces",  # Отображение списка связанных маркетплейсов (через метод)
        "updated_at",  # Отображение даты последнего обновления записи
    )

    def get_products(self, obj):
        """
        Возвращает строку с названиями всех связанных продуктов.

        Args:
            obj (Searchers): Экземпляр поискового запроса.

        Returns:
            str: Названия продуктов через запятую.
        """
        return ", ".join([product.name for product in obj.products.all()])
    get_products.short_description = "Продукты"  # Заголовок столбца в списке

    def get_marketplaces(self, obj):
        """
        Возвращает строку с названиями всех связанных маркетплейсов.

        Args:
            obj (Searchers): Экземпляр поискового запроса.

        Returns:
            str: Названия маркетплейсов через запятую.
        """
        return ", ".join([marketplace.name for marketplace in obj.marketplaces.all()])
    get_marketplaces.short_description = "Маркетплейсы"  # Заголовок столбца в списке

    """Фильтры, доступные в правой колонке списка поисковых запросов."""
    list_filter = ("user", )  # Фильтр по пользователю

    """Поля, по которым можно искать поисковые запросы в админке."""
    search_fields = ("user",)  # Поля для поиска поисковых запросов

    """Текст‑подсказка, отображаемый под полем поиска."""
    search_help_text = "Введите имя пользователя для поиска"  # Подсказка для поля поиска

    """Сортировка списка поисковых запросов по дате создания."""
    ordering = ("-created_at",)  # Сортировка по дате создания (новые сверху)

    """Добавление навигации по датам в верхней части списка запросов."""
    date_hierarchy = "created_at"  # Иерархия дат по полю created_at

    """Поля, которые нельзя редактировать в админ‑панели."""
    readonly_fields = ("created_at", "updated_at")  # Поля, доступные только для чтения

    """Поля, отображаемые в форме редактирования/создания поискового запроса."""
    fields = (
        "user",  # Пользователь, сделавший запрос
        "query",  # Текст поискового запроса
        "products",  # Связанные продукты
        "marketplaces",  # Связанные маркетплейсы
    )
