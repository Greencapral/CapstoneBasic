from django.contrib import admin

from web_scraping.models import Marketplace, Product, Searchers



@admin.register(Marketplace)
class MarketplaceAdmin(admin.ModelAdmin):
    """
    Админ‑панель для модели Marketplace (маркетплейсы).
    Позволяет управлять списком торговых площадок: просматривать, искать и фильтровать.
    """

    """Поля, отображаемые в списке записей маркетплейсов."""
    list_display = (
        "name",  # Отображение названия маркетплейса
        "base_url",  # Отображение базового URL площадки
    )

    """Фильтры, доступные в правой колонке списка маркетплейсов."""
    list_filter = ("name",)  # Фильтр по названию маркетплейса

    """Поля, по которым можно искать маркетплейсы в админке."""
    search_fields = ("name",)  # Поля для поиска маркетплейсов

    """Текст‑подсказка, отображаемый под полем поиска."""
    search_help_text = "Введите наименование площадки для поиска"  # Подсказка для поля поиска

    """Сортировка списка маркетплейсов по дате создания (от новых к старым)."""
    ordering = ("-created_at",)  # Сортировка по дате создания (новые сверху)

    """Добавление навигации по датам (год → месяц → день) в верхней части списка."""
    date_hierarchy = "created_at"  # Иерархия дат по полю created_at

    """Поля, которые нельзя редактировать в админ‑панели (автоматически обновляются)."""
    readonly_fields = ("created_at", "updated_at")  # Поля, доступные только для чтения

    """Поля, отображаемые в форме редактирования/создания маркетплейса."""
    fields = (
        "name",  # Поле названия маркетплейса
        "base_url",  # Поле базового URL маркетплейса
    )



